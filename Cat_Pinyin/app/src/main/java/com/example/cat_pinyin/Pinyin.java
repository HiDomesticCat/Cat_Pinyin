    public static int[] pinyin(int[] SearchArray, int SearchQuantity){
		int[] arr = new int[2]; //儲存[對應發音位置,對應字單詞位置]
		arr[0] = -1;
		arr[1] = -1;
        for(int Row = 0; Row < DictionaryQuantity; Row++){ //循環行
            if(SearchQuantity != PhoneticDictionary[Row][0]){
                continue;
            }
            for(int List=1; true; List++){ //循環列
                //System.out.printf("%d %d %d\n", a, b, B[a][b]);
                if(PhoneticDictionary[Row][List] != SearchArray[List-1]){ //是否相同，是往下，否跳出此列
                    //System.out.printf("a%d %d %d\n", a, b, B[a][b]);
                    break;
                }
                if(List == 4){ //是否為音調格
                    //System.out.printf("%d\n", PhoneticDictionary[Row][3]);
					arr[0] = PhoneticDictionary[Row][5];
					arr[1] = PhoneticDictionary[Row][6];
                    return arr;
                }
            }
        }
        return arr;
    }
